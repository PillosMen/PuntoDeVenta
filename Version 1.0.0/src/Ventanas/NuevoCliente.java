    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        String nombre, direccion, telefono, apellido;
        
        String sql;
        
        nombre = nom.getText();
        apellido = ape.getText();
        telefono = tel.getText();
        direccion = dir.getText();
        
        sql="INSERT INTO clientes (nombre, apellido, telefono, domicilio) VALUES(?,?,?,?)";
        
        try {
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setString(1, nombre);
            ps.setString(2, apellido);
            ps.setString(3, telefono);
            ps.setString(4, direccion);
          
            int n = ps.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(this, "Se Guardo Con Exito!!!", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);
            
            limpiar();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_GuardarActionPerformed

